
function lf(){
  grep -n -E -i $1 $2 -C $3 --color=auto --group-separator='#######--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#######';
}


function lfr(){
  grep -n -E -i $1 -r --include=$2 -C $3 --color=auto  --group-separator='#######-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------########' ./ ;
}


function lfc(){
  grep -n -E -i $1 -r --include=$2 -c --color=auto ./;
}

function f(){
	find $1 -exec $2 {} \;
}



